import UIKit

extension UIColor {
    static let aBlack: UIColor = UIColor(named: "aBlack") ?? .black
    static let aRed: UIColor = UIColor(named: "aRed") ?? .red
    static let aGreen: UIColor = UIColor(named: "aGreen") ?? .green
    static let aBlue: UIColor = UIColor(named: "aBlue") ?? .blue
    static let aBlue2: UIColor = UIColor(named: "aBlue2") ?? .blue
    static let aCyan: UIColor = UIColor(named: "aCyan") ?? .cyan
}
